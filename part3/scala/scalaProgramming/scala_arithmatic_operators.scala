object scala_arithmatic_operators {
  def main(args: Array[String]): Unit = {
    val a = 10
    val b = 20
    val c = 25
    val d = 25

    println("a + b = " + (a + b))
    println("a - b = " + (a - b))
    println("a * b = " + (a * b))
    println("b / a = " + (b / a))
    println("b % a = " + (b % a))
    println("c % a = " + (c % a))
  }
}
